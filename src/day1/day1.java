package day1;

public class day1 {
    /*
     * Javaを使った最も基本的なプログラム
     */
    public static void main(String[] args) {
        //「Hello,World」という文字列を表示します。
        System.out.println("Hello,World");
        // 数値の表示
        System.out.print(123);
        System.out.println(456);
        // 文字列の表示
        System.out.print("ABC");
        System.out.println("DEF");
    }
}
